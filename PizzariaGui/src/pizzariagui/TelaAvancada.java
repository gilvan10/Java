    public void carregarListaIngredientes(){
        
         File currentDir = new File("./plugins");
         String []plugins = currentDir.list();
         int i;
         String nome;
         for (i = 0; i < plugins.length; i++){
              nome = plugins[i].split("\\.")[0];
	      ingredientes.add(nome);
   
         }
        
    }
    

