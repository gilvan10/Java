    private void btnMoverPraCimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMoverPraCimaActionPerformed
        String elementoSustituido; 
        String elementoSustituidor; 
        int indice = jListOpcoesEscolhidas.getSelectedIndex();
        if(indice != 0) {
           elementoSustituidor = (String) escolhas.get(indice);
           elementoSustituido = (String) escolhas.get(indice-1);
           escolhas.set(indice-1,elementoSustituidor);
           escolhas.set(indice,elementoSustituido);
         }
            
    }//GEN-LAST:event_btnMoverPraCimaActionPerformed

    private void btnMoverPraBaixoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMoverPraBaixoActionPerformed
        String elementoSustituido; 
        String elementoSustituidor; 
        int indice = jListOpcoesEscolhidas.getSelectedIndex();
        if(indice < escolhas.size()-1) {
           elementoSustituidor = (String) escolhas.get(indice);
           elementoSustituido = (String) escolhas.get(indice+1);
           escolhas.set(indice+1,elementoSustituidor);
           escolhas.set(indice,elementoSustituido);
        }
    }//GEN-LAST:event_btnMoverPraBaixoActionPerformed

    private void btnConcluirPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcluirPedidoActionPerformed
        ArrayList<PizzaDecorator> ingredientesDecorator = new ArrayList<>();
        PizzaDecorator auxDecorator;
        PizzaSimples auxSimples;
        File currentDir = new File("./plugins");
        String []plugins = currentDir.list();
        int i;
        URL[] jars = new URL[plugins.length];
        for(i = 0; i < plugins.length; i++) {
            try {
                jars[i] = (new File("./plugins/" + plugins[i])).toURL();
            } catch (MalformedURLException ex) {
                Logger.getLogger(TelaAvancada.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        URLClassLoader ulc = new URLClassLoader(jars);
        
        String pedido;
        for(i = 0; i < escolhas.size(); i++) {
            pedido = (String) escolhas.get(i);
            try {
                ingredientesDecorator.add((PizzaDecorator) Class.forName(pedido.toLowerCase() + "." + pedido, true, ulc).newInstance());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(TelaAvancada.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InstantiationException ex) {
                Logger.getLogger(TelaAvancada.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IllegalAccessException ex) {
                Logger.getLogger(TelaAvancada.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        for(i = 0; i < ingredientesDecorator.size()-1; i++){
            auxDecorator = (PizzaDecorator) ingredientesDecorator.get(i);
            auxDecorator.setDecorator((PizzaDecorator)ingredientesDecorator.get(i+1));
        }
        auxSimples = new PizzaSimples();
        if(!ingredientesDecorator.isEmpty()){
           auxDecorator = (PizzaDecorator) ingredientesDecorator.get(i);
           auxDecorator.setDecorator(auxSimples);
           System.out.println(((PizzaDecorator) ingredientesDecorator.get(0)).adicionar());
        }
        else
            System.out.println(auxSimples.adicionar());
        
    }//GEN-LAST:event_btnConcluirPedidoActionPerformed

